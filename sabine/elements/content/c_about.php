        <h3>Idee</h3>
        <p>
            Ziel war es, ein interaktives Musikerlebnis zu schaffen, das direkt im Browser zur Musik generiert wird und damit immer 
            ein individuelles Aussehen hat. Elements besch&auml;ftigt sich mit der Thematik der Elemente Feuer, Wasser und Erde, die in 
            ihrer Charakteristik musikalisch umgesetzt und grafisch dargestellt werden.
        </p>
        <p>Zu jedem Element gibt es eine eigene dreidimensionale 
            Welt mit Musik, Farben und Formen, die beliebig durchquert werden kann. Jedes Element hat seine spezifischen Attribute: Feuer gilt 
            als wild und leidenschaftlich, w&auml;hrend Wasser f&uuml;r das Fließende und die Leichtigkeit steht, Erde hingegen enth&auml;lt die Ruhe, Statik und das Verl&auml;ssliche.
            Die Zusammengeh&ouml;rigkeit der Elemente wird unterstrichen, indem man zwischen den Welten frei wechseln kann. 
        </p>
        <h3>Technische Zielsetzung</h3>
        <p>
            Die technische Zielsetzung war eine HTML5 Website mit Schwerpunkt auf WebGL, die &uuml;ber die Library three.js bedient wird. 
            Die Optimierung der Seite wurde f&uuml;r den Browser Chrome auf einem iMac (CPU 3.4 GHz Intel Core i7, GPU 2 GB) vorgenommen. 
            Ein Rechner mit CPU 2.7 GHz Intel Core i7 und GPU 512 MB ist die Mindestvoraussetzung. 
            Bei der verwendeten Technologie WebGL handelt es sich um einen offenen Webstandard, der es erm&ouml;glicht, 3D-Szenen ohne Plugin im Browser zu rendern.
            Als Vorbild f&uuml;r dieses Vorhaben kann das Projekt “Lights von Ellie Goulding” genannt werden, dessen technische Umsetzung von Carlos Ulloa vorgenommen wurde.
        </p>
        <p>
            Die Herausforderung in der Umsetzung lag f&uuml;r den Bereich der Webentwicklung in der Arbeit mit einer neuen Technologie und in der 
            Synchronisation der Welten zur Musik. Parallel dazu stand die Usability im Vordergrund.
        </p>
        <h3>Audio</h3>
        <p>
            Ziel der Audiostudenten war die Stimmung der jeweiligen Welt mit der Musik auszudr&uuml;cken. Zus&auml;tzlich sollten die Tracks vielf&auml;ltige M&ouml;glichkeiten f&uuml;r die visuellen Effekte bieten.
            Dabei entstanden verschiedene, treffende Interpretationen. Das Endergebnis sind insgesamt neun Soundtracks f&uuml;r drei Welten, die als MP3&ndash; und OGG&ndash; kodierte Dateien eingebunden wurden.
        </p>
        <p>F&uuml;r die Erstellung der Songs kamen verschiedene Sequencer wie Avid Pro Tools, Apple Logic Pro und Live von Ableton zum Einsatz. 
            Mittels Synthesizer und Sampler wurden Kl&auml;nge wie zum Beispiel Oboen, Hangdrums oder Violinen gestaltet.
            Die Challenge bei der Umsetzung dieses Projekts lag in der Vermittlung der passenden Stimmung innerhalb von ca. 90 Sekunden pro Element.
        </p>
